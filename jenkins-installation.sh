#!/bin/bash
# Jenkins installation process
echo "############################################################"
echo "****************Install Pre-requisites********************"
echo "############################################################"
yum install java-11* -y
yum install wget -y
yum install zip -y
echo "############################################################"
echo "****************Validate Java version********************"
echo "############################################################"
java --version
echo "############################################################"
echo "****************Enable port 8080 for outside of world********************"
echo "############################################################"
firewall-cmd --permanent --add-port=8080/tcp
firewall-cmd --reload
echo "############################################################"
echo "****************Download Jenkins Package********************"
echo "############################################################"
cd /opt
wget  https://get.jenkins.io/war-stable/2.440.2/jenkins.war
echo "############################################################"
echo "****************Run the Jenkins service********************"
echo "############################################################"
cd /opt/
java -jar jenkins.war
